from datetime import datetime, timedelta, timezone
from constants.time_constants import MOSCOW_TZ, RU_DAYS, RU_MONTHS
from app.logger import logger


def parse_datetime(text: str):
    """
    –ü—ã—Ç–∞–µ—Ç—Å—è —Ä–∞—Å–ø–∞—Ä—Å–∏—Ç—å –¥–∞—Ç—É –∏ –≤—Ä–µ–º—è –∏–∑ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å—Å–∫–æ–≥–æ –≤–≤–æ–¥–∞.

    –§—É–Ω–∫—Ü–∏—è –ø—Ä–∏–Ω–∏–º–∞–µ—Ç —Å—Ç—Ä–æ–∫—É, –≤–≤–µ–¥—ë–Ω–Ω—É—é –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–º, –∏ –ø–æ–æ—á–µ—Ä—ë–¥–Ω–æ
    –ø—ã—Ç–∞–µ—Ç—Å—è –∏–Ω—Ç–µ—Ä–ø—Ä–µ—Ç–∏—Ä–æ–≤–∞—Ç—å –µ—ë –≤ –æ–¥–Ω–æ–º –∏–∑ –ø–æ–¥–¥–µ—Ä–∂–∏–≤–∞–µ–º—ã—Ö —Ñ–æ—Ä–º–∞—Ç–æ–≤.
    –ò—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è –¥–ª—è –ø–µ—Ä–≤–∏—á–Ω–æ–≥–æ (–º—è–≥–∫–æ–≥–æ) –ø–∞—Ä—Å–∏–Ω–≥–∞ –±–µ–∑ —Å—Ç—Ä–æ–≥–æ–π –≤–∞–ª–∏–¥–∞—Ü–∏–∏
    –Ω–∞ –±—É–¥—É—â–µ–µ –≤—Ä–µ–º—è.

    –ü–æ–¥–¥–µ—Ä–∂–∏–≤–∞–µ–º—ã–µ —Ñ–æ—Ä–º–∞—Ç—ã:
    1. –°—Ç—Ä–æ–≥–∏–π —Ñ–æ—Ä–º–∞—Ç –¥–∞—Ç—ã –∏ –≤—Ä–µ–º–µ–Ω–∏: YYYY-MM-DD HH:MM
    2. –û—Ç–Ω–æ—Å–∏—Ç–µ–ª—å–Ω—ã–µ –¥–∞—Ç—ã:
       - "—Å–µ–≥–æ–¥–Ω—è HH:MM"
       - "–∑–∞–≤—Ç—Ä–∞ HH:MM"

    –ï—Å–ª–∏ —Å—Ç—Ä–æ–∫—É –Ω–µ–≤–æ–∑–º–æ–∂–Ω–æ —Ä–∞—Å–ø–∞—Ä—Å–∏—Ç—å –Ω–∏ –æ–¥–Ω–∏–º —Å–ø–æ—Å–æ–±–æ–º,
    —Ñ—É–Ω–∫—Ü–∏—è –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç None.

    Args:
        text (str): –°—Ç—Ä–æ–∫–∞ —Å –¥–∞—Ç–æ–π –∏ –≤—Ä–µ–º–µ–Ω–µ–º, –≤–≤–µ–¥—ë–Ω–Ω–∞—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–º.

    Returns:
        datetime | None: –û–±—ä–µ–∫—Ç datetime –≤ –º–æ—Å–∫–æ–≤—Å–∫–æ–º —á–∞—Å–æ–≤–æ–º –ø–æ—è—Å–µ
        –ø—Ä–∏ —É—Å–ø–µ—à–Ω–æ–º –ø–∞—Ä—Å–∏–Ω–≥–µ –∏–ª–∏ None –ø—Ä–∏ –æ—à–∏–±–∫–µ.
    """
    # –ü—Ä–∏–≤–æ–¥–∏–º —Å—Ç—Ä–æ–∫—É –∫ –Ω–∏–∂–Ω–µ–º—É —Ä–µ–≥–∏—Å—Ç—Ä—É –∏ —É–¥–∞–ª—è–µ–º –ø—Ä–æ–±–µ–ª—ã –ø–æ –∫—Ä–∞—è–º
    # –¥–ª—è —É–Ω–∏—Ñ–∏–∫–∞—Ü–∏–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å—Å–∫–æ–≥–æ –≤–≤–æ–¥–∞
    text = text.strip().lower()

    # –¢–µ–∫—É—â–µ–µ –≤—Ä–µ–º—è –≤ –º–æ—Å–∫–æ–≤—Å–∫–æ–º —á–∞—Å–æ–≤–æ–º –ø–æ—è—Å–µ
    now = datetime.now(MOSCOW_TZ)

    logger.debug('–ó–∞–ø—É—Å–∫ –ø–∞—Ä—Å–∏–Ω–≥–∞ –¥–∞—Ç—ã')

    # ------------------------------------------------------------------
    # 1. –°—Ç—Ä–æ–≥–∏–π —Ñ–æ—Ä–º–∞—Ç: YYYY-MM-DD HH:MM
    # ------------------------------------------------------------------
    try:
        logger.debug('–ü–∞—Ä—Å–∏–Ω–≥ –¥–∞—Ç—ã –ø—Ä–æ—à—ë–ª —É—Å–ø–µ—à–Ω–æ')
        return datetime.strptime(text, "%Y-%m-%d %H:%M")
    except ValueError:
        # –ï—Å–ª–∏ —Ñ–æ—Ä–º–∞—Ç –Ω–µ –ø–æ–¥–æ—à—ë–ª ‚Äî –ø–µ—Ä–µ—Ö–æ–¥–∏–º –∫ —Å–ª–µ–¥—É—é—â–∏–º –≤–∞—Ä–∏–∞–Ω—Ç–∞–º
        pass

    # ------------------------------------------------------------------
    # 2. –û—Ç–Ω–æ—Å–∏—Ç–µ–ª—å–Ω–∞—è –¥–∞—Ç–∞: "—Å–µ–≥–æ–¥–Ω—è HH:MM"
    # ------------------------------------------------------------------
    if text.startswith("—Å–µ–≥–æ–¥–Ω—è"):
        # –û—Ç–¥–µ–ª—è–µ–º —á–∞—Å—Ç—å —Å–æ –≤—Ä–µ–º–µ–Ω–µ–º
        time_part = text.replace("—Å–µ–≥–æ–¥–Ω—è", "").strip()
        try:
            logger.debug('–ü–∞—Ä—Å–∏–Ω–≥ –¥–∞—Ç—ã –ø—Ä–æ—à—ë–ª —É—Å–ø–µ—à–Ω–æ')
            hour, minute = map(int, time_part.split(":"))

            # –ü–æ–¥—Å—Ç–∞–≤–ª—è–µ–º –≤–≤–µ–¥—ë–Ω–Ω–æ–µ –≤—Ä–µ–º—è –≤ —Ç–µ–∫—É—â—É—é –¥–∞—Ç—É
            return now.replace(hour=hour, minute=minute, second=0, microsecond=0)
        except ValueError:
            pass

    # ------------------------------------------------------------------
    # 3. –û—Ç–Ω–æ—Å–∏—Ç–µ–ª—å–Ω–∞—è –¥–∞—Ç–∞: "–∑–∞–≤—Ç—Ä–∞ HH:MM"
    # ------------------------------------------------------------------
    if text.startswith("–∑–∞–≤—Ç—Ä–∞"):
        # –û—Ç–¥–µ–ª—è–µ–º —á–∞—Å—Ç—å —Å–æ –≤—Ä–µ–º–µ–Ω–µ–º
        time_part = text.replace("–∑–∞–≤—Ç—Ä–∞", "").strip()
        try:
            logger.info('–ü–∞—Ä—Å–∏–Ω–≥ –¥–∞—Ç—ã –ø—Ä–æ—à—ë–ª —É—Å–ø–µ—à–Ω–æ')
            hour, minute = map(int, time_part.split(":"))

            # –£–≤–µ–ª–∏—á–∏–≤–∞–µ–º —Ç–µ–∫—É—â—É—é –¥–∞—Ç—É –Ω–∞ –æ–¥–∏–Ω –¥–µ–Ω—å –∏ –ø–æ–¥—Å—Ç–∞–≤–ª—è–µ–º –≤—Ä–µ–º—è
            return (now + timedelta(days=1)).replace(
                hour=hour, minute=minute, second=0, microsecond=0
            )
        except ValueError:
            pass

    # –ï—Å–ª–∏ –Ω–∏ –æ–¥–∏–Ω –∏–∑ –≤–∞—Ä–∏–∞–Ω—Ç–æ–≤ –Ω–µ —Å—Ä–∞–±–æ—Ç–∞–ª ‚Äî –≤–æ–∑–≤—Ä–∞—â–∞–µ–º None
    logger.debug('–ü–∞—Ä—Å–∏–Ω–≥ –¥–∞—Ç—ã %s –ø—Ä–æ—à—ë–ª –Ω–µ—É—Å–ø–µ—à–Ω–æ', text)
    return None


def format_task_date(dt_or_str) -> str:
    """
    –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ—Ç –¥–∞—Ç—É –∑–∞–¥–∞—á–∏ –≤ —á–µ–ª–æ–≤–µ–∫–æ—á–∏—Ç–∞–µ–º—ã–π —Ñ–æ—Ä–º–∞—Ç –Ω–∞ —Ä—É—Å—Å–∫–æ–º —è–∑—ã–∫–µ.

    –§—É–Ω–∫—Ü–∏—è –ø—Ä–∏–Ω–∏–º–∞–µ—Ç –ª–∏–±–æ –æ–±—ä–µ–∫—Ç datetime, –ª–∏–±–æ —Å—Ç—Ä–æ–∫—É –≤ ISO-—Ñ–æ—Ä–º–∞—Ç–µ,
    –ø—Ä–∏–≤–æ–¥–∏—Ç –¥–∞—Ç—É –∫ –º–æ—Å–∫–æ–≤—Å–∫–æ–º—É —á–∞—Å–æ–≤–æ–º—É –ø–æ—è—Å—É –∏ —Ñ–æ—Ä–º–∞—Ç–∏—Ä—É–µ—Ç –µ—ë
    –≤ —É–¥–æ–±–Ω—ã–π –¥–ª—è –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é –≤–∏–¥.

    –§–æ—Ä–º–∞—Ç —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞:
    "<–î–ï–ù–¨_–ù–ï–î–ï–õ–ò>, DD <–ú–ï–°–Ø–¶> YYYY HH:MM"

    –ü—Ä–∏–º–µ—Ä:
    "–í–¢, 01 –û–∫—Ç 2222 10:00"

    Args:
        dt_or_str (datetime | str): –î–∞—Ç–∞ –∑–∞–¥–∞—á–∏ –≤ –≤–∏–¥–µ datetime
        –∏–ª–∏ —Å—Ç—Ä–æ–∫–∏ –≤ ISO-—Ñ–æ—Ä–º–∞—Ç–µ.

    Returns:
        str: –û—Ç—Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–Ω–∞—è —Å—Ç—Ä–æ–∫–∞ —Å –¥–∞—Ç–æ–π –∏ –≤—Ä–µ–º–µ–Ω–µ–º –∑–∞–¥–∞—á–∏.

    Raises:
        TypeError: –ï—Å–ª–∏ –ø–µ—Ä–µ–¥–∞–Ω –∞—Ä–≥—É–º–µ–Ω—Ç –Ω–µ–ø–æ–¥–¥–µ—Ä–∂–∏–≤–∞–µ–º–æ–≥–æ —Ç–∏–ø–∞.
    """
    # –ï—Å–ª–∏ –¥–∞—Ç–∞ –ø—Ä–∏—à–ª–∞ –≤ –≤–∏–¥–µ —Å—Ç—Ä–æ–∫–∏ ‚Äî –ø—ã—Ç–∞–µ–º—Å—è —Ä–∞—Å–ø–∞—Ä—Å–∏—Ç—å ISO-—Ñ–æ—Ä–º–∞—Ç
    if isinstance(dt_or_str, str):
        dt = datetime.fromisoformat(dt_or_str.replace("Z", "+00:00"))
    elif isinstance(dt_or_str, datetime):
        dt = dt_or_str
    else:
        # –ó–∞—â–∏—Ç–∞ –æ—Ç –Ω–µ–∫–æ—Ä—Ä–µ–∫—Ç–Ω—ã—Ö —Ç–∏–ø–æ–≤ –¥–∞–Ω–Ω—ã—Ö
        logger.error(
            "–û—à–∏–±–∫–∞ –ø—Ä–∏ –ø–æ–ø—ã—Ç–∫–µ –ø—Ä–∏–æ–±—Ä–∞–∑–æ–≤–∞—Ç—å datetime | ISO-—Å—Ç—Ä–æ–∫—É %s",
            dt_or_str
        )
        raise TypeError(f"Expected str or datetime, got {type(dt_or_str)}")

    # –ï—Å–ª–∏ tzinfo –æ—Ç—Å—É—Ç—Å—Ç–≤—É–µ—Ç ‚Äî —Å—á–∏—Ç–∞–µ–º –¥–∞—Ç—É UTC
    if dt.tzinfo is None:
        dt = dt.replace(tzinfo=timezone.utc)

    # –ö–æ–Ω–≤–µ—Ä—Ç–∏—Ä—É–µ–º –¥–∞—Ç—É –≤–æ –≤—Ä–µ–º–µ–Ω–Ω—É—é –∑–æ–Ω—É –ú–æ—Å–∫–≤—ã
    dt_local = dt.astimezone(MOSCOW_TZ)

    # –ü–æ–ª—É—á–∞–µ–º –Ω–∞–∑–≤–∞–Ω–∏–µ –¥–Ω—è –Ω–µ–¥–µ–ª–∏ –∏ –º–µ—Å—è—Ü–∞ –Ω–∞ —Ä—É—Å—Å–∫–æ–º —è–∑—ã–∫–µ
    day_name = RU_DAYS[dt_local.weekday()]
    month_name = RU_MONTHS[dt_local.month - 1]

    # –§–æ—Ä–º–∏—Ä—É–µ–º —Ñ–∏–Ω–∞–ª—å–Ω—É—é —Å—Ç—Ä–æ–∫—É –¥–∞—Ç—ã
    return (
        f"{day_name}, {dt_local.day:02d} {month_name} "
        f"{dt_local.year} {dt_local.hour:02d}:{dt_local.minute:02d}"
    )


def format_task(task: dict) -> str:
    """
    –§–æ—Ä–º–∞—Ç–∏—Ä—É–µ—Ç –∑–∞–¥–∞—á—É –¥–ª—è –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é –≤ —Å–æ–æ–±—â–µ–Ω–∏–∏ Telegram.

    –§—É–Ω–∫—Ü–∏—è –ø—Ä–∏–Ω–∏–º–∞–µ—Ç —Å–ª–æ–≤–∞—Ä—å –∑–∞–¥–∞—á–∏, —Ñ–æ—Ä–º–∞—Ç–∏—Ä—É–µ—Ç –¥–∞—Ç—É –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è
    –∏ —Å–æ–±–∏—Ä–∞–µ—Ç –∏—Ç–æ–≥–æ–≤—É—é —Å—Ç—Ä–æ–∫—É —Å —ç–º–æ–¥–∑–∏ –∏ —á–∏—Ç–∞–µ–º—ã–º –ø—Ä–µ–¥—Å—Ç–∞–≤–ª–µ–Ω–∏–µ–º
    –∑–∞–¥–∞—á–∏.

    Args:
        task (dict): –°–ª–æ–≤–∞—Ä—å –∑–∞–¥–∞—á–∏, —Å–æ–¥–µ—Ä–∂–∞—â–∏–π –∫–∞–∫ –º–∏–Ω–∏–º—É–º
        –∫–ª—é—á–∏ 'title' –∏ 'scheduled_time'.

    Returns:
        str: –ì–æ—Ç–æ–≤–∞—è —Å—Ç—Ä–æ–∫–∞ –¥–ª—è –æ—Ç–ø—Ä–∞–≤–∫–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é.
    """
    # –§–æ—Ä–º–∞—Ç–∏—Ä—É–µ–º –¥–∞—Ç—É –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è –∑–∞–¥–∞—á–∏
    date_str = format_task_date(task["scheduled_time"])

    # –§–æ—Ä–º–∏—Ä—É–µ–º –∏—Ç–æ–≥–æ–≤—ã–π —Ç–µ–∫—Å—Ç –∑–∞–¥–∞—á–∏
    return f"üìù {task['title']}\n‚è∞ {date_str}"


def parse_and_validate_datetime(text: str) -> datetime | None:
    """
    –ü–∞—Ä—Å–∏—Ç –¥–∞—Ç—É –∏–∑ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å—Å–∫–æ–≥–æ –≤–≤–æ–¥–∞ –∏ –ø—Ä–æ–≤–µ—Ä—è–µ—Ç,
    —á—Ç–æ –æ–Ω–∞ –Ω–∞—Ö–æ–¥–∏—Ç—Å—è –≤ –±—É–¥—É—â–µ–º.

    –§—É–Ω–∫—Ü–∏—è —è–≤–ª—è–µ—Ç—Å—è –æ–±—ë—Ä—Ç–∫–æ–π –Ω–∞–¥ parse_datetime():
    - —Å–Ω–∞—á–∞–ª–∞ –≤—ã–ø–æ–ª–Ω—è–µ—Ç—Å—è –ø–∞—Ä—Å–∏–Ω–≥ –¥–∞—Ç—ã;
    - –∑–∞—Ç–µ–º –¥–∞—Ç–∞ –ø—Ä–∏–≤–æ–¥–∏—Ç—Å—è –∫ UTC;
    - –ø–æ—Å–ª–µ —ç—Ç–æ–≥–æ –ø—Ä–æ–≤–µ—Ä—è–µ—Ç—Å—è, —á—Ç–æ –¥–∞—Ç–∞ –±–æ–ª—å—à–µ —Ç–µ–∫—É—â–µ–≥–æ –≤—Ä–µ–º–µ–Ω–∏.

    –ò—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è –ø—Ä–∏ —Å–æ–∑–¥–∞–Ω–∏–∏ –∏ –ø–µ—Ä–µ–Ω–æ—Å–µ –∑–∞–¥–∞—á,
    —á—Ç–æ–±—ã –∏—Å–∫–ª—é—á–∏—Ç—å –¥–æ–±–∞–≤–ª–µ–Ω–∏–µ –∑–∞–¥–∞—á –≤ –ø—Ä–æ—à–ª–æ–º.

    Args:
        text (str): –°—Ç—Ä–æ–∫–∞ —Å –¥–∞—Ç–æ–π –∏ –≤—Ä–µ–º–µ–Ω–µ–º, –≤–≤–µ–¥—ë–Ω–Ω–∞—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–º.

    Returns:
        datetime | None: –û–±—ä–µ–∫—Ç datetime –≤ UTC –ø—Ä–∏ —É—Å–ø–µ—à–Ω–æ–º –ø–∞—Ä—Å–∏–Ω–≥–µ
        –∏ –≤–∞–ª–∏–¥–∞—Ü–∏–∏ –∏–ª–∏ None, –µ—Å–ª–∏ –¥–∞—Ç–∞ –Ω–µ–∫–æ—Ä—Ä–µ–∫—Ç–Ω–∞ –∏–ª–∏ —É–∂–µ –ø—Ä–æ—à–ª–∞.
    """
    logger.debug('–ü–∞—Ä—Å–∏–Ω–≥ –∏ –≤–∞–ª–∏–¥–∞—Ü–∏—è –¥–∞—Ç—ã, –≤–≤–µ–¥—ë–Ω–Ω–æ–π –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–º...')

    # –í—ã–ø–æ–ª–Ω—è–µ–º –ø–µ—Ä–≤–∏—á–Ω—ã–π –ø–∞—Ä—Å–∏–Ω–≥ –¥–∞—Ç—ã
    dt = parse_datetime(text)
    if not dt:
        logger.debug('–ü–∞—Ä—Å–∏–Ω–≥ –∏ –≤–∞–ª–∏–¥–∞—Ü–∏—è –¥–∞—Ç—ã –∑–∞–≤–µ—Ä—à–∏–ª–∏—Å—å –Ω–µ—É—Å–ø–µ—à–Ω–æ')
        return None

    # –ü—Ä–∏–≤–æ–¥–∏–º –¥–∞—Ç—É –∫ UTC –¥–ª—è –¥–∞–ª—å–Ω–µ–π—à–µ–π —É–Ω–∏—Ñ–∏—Ü–∏—Ä–æ–≤–∞–Ω–Ω–æ–π —Ä–∞–±–æ—Ç—ã
    dt_utc = dt.replace(tzinfo=MOSCOW_TZ).astimezone(timezone.utc)

    # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ –¥–∞—Ç–∞ –Ω–∞—Ö–æ–¥–∏—Ç—Å—è –≤ –±—É–¥—É—â–µ–º
    if dt_utc < datetime.now(timezone.utc):
        logger.debug('–ü–∞—Ä—Å–∏–Ω–≥ –¥–∞—Ç—ã –∑–∞–≤–µ—Ä—à–∏–ª—Å—è —É—Å–ø–µ—à–Ω–æ, –≤–∞–ª–∏–¥–∞—Ü–∏—è –Ω–µ –ø—Ä–æ–π–¥–µ–Ω–∞')
        return None

    logger.debug('–ü–∞—Ä—Å–∏–Ω–≥ –∏ –≤–∞–ª–∏–¥–∞—Ü–∏—è –¥–∞—Ç—ã –∑–∞–≤–µ—Ä—à–∏–ª–∏—Å—å —É—Å–ø–µ—à–Ω–æ')
    return dt_utc
