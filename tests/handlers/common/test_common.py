    assert len(kb.inline_keyboard) == 1

    # –ü–æ–ª—É—á–∞–µ–º –∫–Ω–æ–ø–∫—É –∏–∑ –∫–ª–∞–≤–∏–∞—Ç—É—Ä—ã
